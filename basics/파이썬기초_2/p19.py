# 두 딕셔너리를 중복 제거된 하나의 딕셔너리로 합친 후 가격 3000원 이상인 메뉴만 출력

a = {'아메리카노': 1900, '카페모카': 3300, '에스프레소': 1900, '카페라떼': 2500, '카푸치노': 2500, '바닐라라떼': 2900}
b = {'헤이즐럿라떼': 2900, '카페모카': 3300, '밀크커피': 3300, '아메리카노': 1900, '샷크린티라떼': 3300}

a.update(b)

dict_menu = list(a.items())

dict_3000 = []

for i in range(len(dict_menu)):
    if dict_menu[i][1] > 3000:
        dict_3000.append(dict_menu[i])

print(set(dict_3000))
